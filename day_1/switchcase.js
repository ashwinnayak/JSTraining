// switch case is used to map condition to value
var country_name = 'UK'
switch (country_name) {
    case 'UK':
        console.log('United Knigdom')
        break
    case 'In':
        console.log('India')
        break
    case 'US':
        console.log('United States')
        break
    default:
        console.log('Earth')
}